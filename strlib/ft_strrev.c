#include "libft.h"

void	ft_strrev(char *s)
{
	size_t	i;
	size_t	f;
	char	c;

	i = 0;
	f = ft_strlen(s) - 1;
	while (i < f)
	{
		c = s[i];
		s[i] = s[f];
		s[f] = c;
		i++;
		f--;
	}
}
