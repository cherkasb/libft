#include "libft.h"

char		*ft_strdup(const char *s1)
{
	char	*cp;
	size_t	i;

	i = 0;
	cp = (char *)malloc(sizeof(char) * (ft_strlen(s1) + 1));
	if (cp == NULL)
		return (NULL);
	while (s1[i])
	{
		cp[i] = s1[i];
		i++;
	}
	cp[i] = '\0';
	return (cp);
}
