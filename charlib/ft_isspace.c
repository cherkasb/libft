#include "libft.h"

int		ft_isspace(int c)
{
	unsigned char	k;

	k = (unsigned char)c;
	if (k == '\t' || k == '\n' || k == '\v' ||
			k == '\f' || k == '\r' || k == ' ')
		return (1);
	return (0);
}
